# input a float value for variable a here
var_a = float(input("enter variable a here: "))
# input a float value for variable b here
var_b = float(input("enter variable b here: "))

# output the result of addition here
addition = (var_a + var_b)

# output the result of subtraction here
subtraction = (var_a - var_b)

# output the result of multiplication here
multiplication = (var_a * var_b)

# output the result of division here
division = (var_a / var_b)

# output the result of expo here
expo = var_a ** var_b

# output the result of floor-division here
floor_div = var_a // var_b

print("addition result: ", addition)
print("subtraction result: ", subtraction)
print("multiplication result: ", multiplication)
print("division result: ", division)
print("expo result: ", expo)
print("floor_division result: ", floor_div)
