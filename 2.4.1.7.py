#initial requerments for the program
john = 3
mary = 5
adam = 6

# printing three variable togather
print ("John, marry and adam have: ", john, mary, adam, "apples respectively")

# creating new variable "total_apples" and adding all others variables
Total_apples = (john + mary + adam)
print ("Total apples are :", Total_apples)

# average for the "john", "mary", "adam"
# In python terms it called "Mean"
# Mean = sum of all values, and divide the sum by the number of values

Average = (Total_apples / 3)
print ("Average for 'John', 'Mary' and 'Adam' apples :", round(Average, 2))